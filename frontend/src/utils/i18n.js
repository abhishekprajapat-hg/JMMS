function translateToHindi(text) {
  if (HINDI_TEXT_MAP[text]) return HINDI_TEXT_MAP[text]

  for (const rule of HINDI_REGEX_RULES) {
    const match = text.match(rule.regex)
    if (!match) continue
    if (typeof rule.to === 'function') {
      return rule.to(text, ...match.slice(1))
    }
    return rule.to
  }

  return text
}

function translateText(value, language) {
  if (language !== 'hi' || typeof value !== 'string') return value
  if (!value.trim()) return value

  const parts = value.match(/^(\s*)([\s\S]*?)(\s*)$/)
  if (!parts) return translateToHindi(value)

  const [, prefix, core, suffix] = parts
  return `${prefix}${translateToHindi(core)}${suffix}`
}

